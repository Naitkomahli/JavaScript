console.log("For loop");
for (let i = 1; i <= 10; i++){
    console.log("Iterasi ke " + i);
}

console.log("While loop");
let j = 10;
while (j>=1){
    console.log("Iterasi ke " + j);
    j--;
}

console.log("do-while loop"); 
let k = 5;
do {
    console.log("Angka: " + k);
    k--;
} while (k >= 1);
