// Fungsi kuadrat
function kuadrat(angka) {
    return angka * angka;
}

console.log(kuadrat(4)); // Output: 16

// Fungsi sapa
function sapa(nama) {
    return "Halo, " + nama + "!";
}

console.log(sapa("Alice")); // Output: Halo, Alice!

// Fungsi cekGenap
function cekGenap(angka) {
    return angka % 2 === 0;
}

console.log(cekGenap(4)); // Output: true
console.log(cekGenap(5)); // Output: false